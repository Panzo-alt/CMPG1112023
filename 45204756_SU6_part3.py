print("ASCII and Character conversion")

#Obtain data from user(character->ASCII)
first = input("Enter character: ")

#conversion of characters
second = ord(first)

#ASCII value
print("The ASCII value is:" , second)

#Obtain data from user(ASCII->characters)
third = int(input("Enter the ASCII value: "))

#coversion of ASCII
fourth = chr(third)

#Character value
print("The character is: " , fourth)




